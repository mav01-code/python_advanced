# It is a process of combining variables and methods
# Encapsulation by class
class Student:
    name="Akshaya Varshini"
    def displayName(self):
        print(f"Hi, I'm {self.name}")

class Faculty:
    name="Swapna Reddy"
    dept="Administration"
    def displayDetails(self):
        print(f"Hi, I'm {self.name}, dept is: {self.dept}")
